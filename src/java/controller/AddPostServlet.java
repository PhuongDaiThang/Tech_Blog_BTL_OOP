    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession();
        User user = (User) session.getAttribute("cur_user");

        if (user == null) {
            request.getRequestDispatcher("login.jsp");
            return;
        }
        
        String postCatID = request.getParameter("addpostcid");
        String postTitle = request.getParameter("addpostptitle");
        String postContent = request.getParameter("addpostpcontent");
        Part part = request.getPart("addpostpimg");
        Post p = new Post(Integer.parseInt(postCatID), user.getuID(), postTitle, postContent, null, part.getSubmittedFileName(), LocalDateTime.now());
        PostDAO pDAO = new PostDAO();
        pDAO.insertPost(p);
        String path = getServletContext().getRealPath("/") + "pics" + File.separator + part.getSubmittedFileName();
        Helper.saveFile(part.getInputStream(), path);
        request.getRequestDispatcher("user.jsp").forward(request, response);
    }

